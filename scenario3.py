import cplex

# =========================================================
# SETS
# =========================================================
K = range(1, 18)   # products
R = range(1, 6)    # routes
L = range(1, 3)    # truck models

# =========================================================
# MAXIMUM POSSIBLE TRUCKS
# =========================================================
I_max = 25

# =========================================================
# PARAMETERS
# =========================================================

# ---------------------------------------------------------
# Truck capacities (m^3)
# ---------------------------------------------------------
V = {
    1: 84,
    2: 43
}

# ---------------------------------------------------------
# Product package volumes (m^3)
# ---------------------------------------------------------
w = {
     1: 0.010,
     2: 0.076,
     3: 0.033,
     4: 0.027,
     5: 0.036,
     6: 0.228,
     7: 0.107,
     8: 0.018,
     9: 0.045,
    10: 0.082,
    11: 0.015,
    12: 0.058,
    13: 0.142,
    14: 0.205,
    15: 0.094,
    16: 0.062,
    17: 0.128
}

# ---------------------------------------------------------
# Number of items inside one package
# ---------------------------------------------------------
p = {
     1: 4,
     2: 2,
     3: 6,
     4: 6,
     5: 24,
     6: 8,
     7: 12,
     8: 8,
     9: 12,
    10: 4,
    11: 24,
    12: 10,
    13: 6,
    14: 8,
    15: 12,
    16: 12,
    17: 6
}

# ---------------------------------------------------------
# Monthly demand
# ---------------------------------------------------------
d = {
     1: 10400,
     2: 1560,
     3: 4550,
     4: 5200,
     5: 3250,
     6: 650,
     7: 1170,
     8: 3900,
     9: 2860,
    10: 2080,
    11: 9100,
    12: 3380,
    13: 1820,
    14: 1040,
    15: 2340,
    16: 2800,
    17: 1800
}

# ---------------------------------------------------------
# Route risk values
# ---------------------------------------------------------
a = {
    1: 3.50,
    2: 3.55,
    3: 4.05,
    4: 3.65,
    5: 3.90
}

# ---------------------------------------------------------
# Maximum monthly trips
# ---------------------------------------------------------
N = {
    (1,1): 6,
    (2,1): 0,
    (3,1): 4,
    (4,1): 4,
    (5,1): 2,

    (1,2): 3,
    (2,2): 0,
    (3,2): 2,
    (4,2): 1,
    (5,2): 1
}

# ---------------------------------------------------------
# Transportation costs
# ---------------------------------------------------------
C = {
    (1,1): 3200,
    (2,1): 3150,
    (3,1): 4600,
    (4,1): 3500,
    (5,1): 3900,

    (1,2): 2600,
    (2,2): 2550,
    (3,2): 3750,
    (4,2): 2850,
    (5,2): 3200
}

# ---------------------------------------------------------
# Objective weights
# ---------------------------------------------------------
W_r = 1
W_c = 1

# ---------------------------------------------------------
# Big-M
# ---------------------------------------------------------
M = 8500

# =========================================================
# MODEL
# =========================================================
model = cplex.Cplex()
model.objective.set_sense(model.objective.sense.minimize)

# =========================================================
# VARIABLES
# =========================================================
var_names = []
obj = []

# ---------------------------------------------------------
# z_lik
# ---------------------------------------------------------
for l in L:
    for i in range(1, I_max + 1):
        for k in K:

            var_names.append(f"z_{l}_{i}_{k}")
            obj.append(0)

# ---------------------------------------------------------
# x_li
# ---------------------------------------------------------
for l in L:
    for i in range(1, I_max + 1):

        var_names.append(f"x_{l}_{i}")
        obj.append(0)

# ---------------------------------------------------------
# y_rl
# ---------------------------------------------------------
for r in R:
    for l in L:

        var_names.append(f"y_{r}_{l}")

        obj.append(
            W_r * a[r]
            + W_c * C[(r,l)]
        )

# =========================================================
# VARIABLE TYPES
# =========================================================
num_z = len(L) * I_max * len(K)
num_x = len(L) * I_max
num_y = len(R) * len(L)

types = (
    ["I"] * num_z +
    ["B"] * num_x +
    ["I"] * num_y
)

# =========================================================
# VARIABLE CREATION
# =========================================================
model.variables.add(
    names=var_names,
    obj=obj,
    lb=[0] * len(var_names),
    types=types
)

# =========================================================
# DEMAND CONSTRAINT
# =========================================================
for k in K:

    vars_k = []
    coeffs = []

    for l in L:
        for i in range(1, I_max + 1):

            vars_k.append(f"z_{l}_{i}_{k}")
            coeffs.append(p[k])

    model.linear_constraints.add(
        lin_expr=[
            cplex.SparsePair(vars_k, coeffs)
        ],
        senses=["G"],
        rhs=[d[k]]
    )

# =========================================================
# CAPACITY CONSTRAINT
# =========================================================
for l in L:
    for i in range(1, I_max + 1):

        vars_li = []
        coeffs = []

        for k in K:

            vars_li.append(f"z_{l}_{i}_{k}")
            coeffs.append(w[k])

        model.linear_constraints.add(
            lin_expr=[
                cplex.SparsePair(vars_li, coeffs)
            ],
            senses=["L"],
            rhs=[V[l]]
        )

# =========================================================
# LINKING CONSTRAINT
# =========================================================
for l in L:
    for i in range(1, I_max + 1):
        for k in K:

            model.linear_constraints.add(
                lin_expr=[
                    cplex.SparsePair(
                        [
                            f"z_{l}_{i}_{k}",
                            f"x_{l}_{i}"
                        ],
                        [1, -M]
                    )
                ],
                senses=["L"],
                rhs=[0]
            )

# =========================================================
# TRUCK COUNT MATCHING
# =========================================================
for l in L:

    left_vars = [
        f"x_{l}_{i}"
        for i in range(1, I_max + 1)
    ]

    right_vars = [
        f"y_{r}_{l}"
        for r in R
    ]

    model.linear_constraints.add(
        lin_expr=[
            cplex.SparsePair(
                left_vars + right_vars,
                [1] * len(left_vars)
                +
                [-1] * len(right_vars)
            )
        ],
        senses=["E"],
        rhs=[0]
    )
    # =========================================================
# LIMIT MODEL 1 TRUCKS
# =========================================================

model.linear_constraints.add(
    lin_expr=[
        cplex.SparsePair(
            [f"x_1_{i}" for i in range(1, I_max + 1)],
            [1] * I_max
        )
    ],
    senses=["L"],
    rhs=[4]
)

# =========================================================
# ROUTE AVAILABILITY CONSTRAINT
# =========================================================
for r in R:
    for l in L:

        model.linear_constraints.add(
            lin_expr=[
                cplex.SparsePair(
                    [f"y_{r}_{l}"],
                    [1]
                )
            ],
            senses=["L"],
            rhs=[N[(r,l)]]
        )

# =========================================================
# SYMMETRY BREAKING
# =========================================================
for l in L:
    for i in range(1, I_max):

        model.linear_constraints.add(
            lin_expr=[
                cplex.SparsePair(
                    [
                        f"x_{l}_{i}",
                        f"x_{l}_{i+1}"
                    ],
                    [1, -1]
                )
            ],
            senses=["G"],
            rhs=[0]
        )

# =========================================================
# SOLVE MODEL
# =========================================================
model.solve()

# =========================================================
# RESULTS
# =========================================================
print("\n===================================")
print("FINAL EXACT MILP RESULTS")
print("===================================\n")

print(
    "Objective Value =",
    round(model.solution.get_objective_value(), 3)
)

print("\nPositive Variables:\n")

for name, val in zip(
    model.variables.get_names(),
    model.solution.get_values()
):

    if val > 0:
        print(name, "=", val)